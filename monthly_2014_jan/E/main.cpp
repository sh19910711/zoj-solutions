// @snippet<sh19910711/contest-base:solution/solver-area.cpp>
namespace solution {
  class Solver: public SolverInterface {
  public:
    const OutputStorage* solve( const InputStorage* in, OutputStorage* out ) {
      if ( in->N == 1 ) {
        out->result = 1;
      } else {
        out->result = f1(in->N) + f2(in->N);
      }
      return out;
    }

  protected:
    static Int f1( Int N ) {
      Int v = N % 2 == 0 ? N - 1 : N - 2;
      Int res = N % 2 == 0 ? v : v * 2;
      v -= 2;
      while ( v >= 1 ) {
        res += 2 * v;
        v -= 2;
      }
      return res + 1;
    }

    static Int f2( Int N ) {
      Int v = N * N;
      Int res = 0;
      for ( int i = 0; i < N - 1; ++ i ) {
        res += v;
        v -= 2;
      }
      return res;
    }

  };
}

